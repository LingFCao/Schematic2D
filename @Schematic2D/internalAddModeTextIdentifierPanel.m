    obj.parent.Fig, ...
    'style', 'pushbutton', ...
    'units', 'normalized', ...
    'position', [.413, .98, .1, .02], ...
    'backgroundcolor', 'b', ...
    'foregroundcolor', 'w', ...
    'fontsize', 10, ...
    'string', 'keys', ...
    'callback', @obj.internalAssignKeys);

