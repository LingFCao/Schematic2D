function internalAddModeTextIdentifierPanel( obj )
% create an uicontrol object to identify the mode we are on.

obj.ModeIdentifierUi = uicontrol( ...
    obj.parent.Fig, ...
    'style', 'text', ...
    'units', 'normalized', ...
    'position', [.005, .98, .1, .02], ...
    'backgroundcolor', [.9, .9, .9], ...
    'fontsize', 10, ...
    'ForegroundColor', 'b', ...
    'string', obj.currentMode );

obj.CoordinateDisplay = uicontrol( ...
    obj.parent.Fig, ...
    'style', 'text', ...
    'units', 'normalized', ...
    'position', [.107, .98, .1, .02], ...
    'backgroundcolor', [.9, .9, .9], ...
    'fontsize', 10, ...
    'ForegroundColor', 'b', ...
    'string', '     ,      ' );

obj.figExportModeDisp = uicontrol( ...
    obj.parent.Fig, ...
    'style', 'text', ...
    'units', 'normalized', ...
    'position', [.209, .98, .1, .02], ...
    'backgroundcolor', [.0, .0, .0], ...
    'fontsize', 10, ...
    'ForegroundColor', 'w', ...
    'string', 'pdf' );
