obj.saveasPDFListener = addlistener(obj,'saveasPDF', 'PostSet', @obj.internalSaveAsPDFCallback);


% create the import static ui element
obj.importUIDisp = uicontrol( ...
    obj.parent.Fig, ...
    'style', 'pushbutton', ...
    'units', 'normalized', ...
    'position', [.311, .98, .1, .02], ...
    'backgroundcolor', 'r', ...
    'fontsize', 10, ...
    'ForegroundColor', 'g', ...
    'string', 'import', ...
    'callback', @obj.internalImportImageCallback);

% locks ... 
